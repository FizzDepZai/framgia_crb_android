public class ItemMonth {
    private int mMonth;
    private int mYear;

    public ItemMonth(int month, int year) {
        mMonth = month;
        mYear = year;
    }

    public int getMonth() {
        return mMonth;
    }

    public void setMonth(int month) {
        mMonth = month;
    }

    public int getYear() {
        return mYear;
    }

    public void setYear(int year) {
        mYear = year;
    }
}
